package prob01;

public class Member {
	
	private int id;			// 회원 id
	private String name;	// 회원 이름
	private int point;		// 회원 point
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	
	
}